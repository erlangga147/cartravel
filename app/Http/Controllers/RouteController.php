    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    /*
     * Attach Pool to Pool as Routes, as Origin to Destination
     */

    public function store(Request $request)
    {
        //
        $parameters = $request->only('origin','destination','duration');

        $originParam = $parameters['origin'];
        $destParam = $parameters['destination'];
        $durationParam = $parameters['duration'];

        $origin = \App\Pool::where('name', $originParam)->first();
        $destination = \App\Pool::where('name', $destParam)->first();
        
        $origin->destination()->save($destination,array('duration' => $durationParam));

        return $this->response->created();
    }

